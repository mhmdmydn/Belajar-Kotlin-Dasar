fun main() {

    //operator perbandingan
    val a = 100
    val b = 200

    val result: Boolean = a > b
    //mencetak false karena a lebih kecil daripada b
    println(result)

    println(100 >= 100)
    println(100 <= 100)

    println("a" > "b")
    println("a" == "a")
    println("a" != "b")
}